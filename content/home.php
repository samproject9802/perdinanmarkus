        <p>Jangankan kopi Lintong, kopi Mandheling saja jarang disebut
            apalagi kopi Lintong. Artinya adalah kopi Lintong juga masih
            belum familiar di masyarakat Sumatera Utara sendiri, namun
            ia malah begitu terkenal bersama kedua rekannya di pasar Internasional.
        </p>
        </br>
        <p>Awal mula adanya kopi Lintong adalah ketika jaman penjajahan dahulu.
            Berkat kolonial Belanda yang membawa bibit kopi Arabika ke daerah
            Lintong Nihuta, Humbang Hasundutan pada tahun 1888.
            Saat itu kopi hanya ditanam pada wilayah pegunungan Bukit Barisan
            dan dekat Danau Toba yang ketinggian dataran cocok untuk ditanami kopi.
        </p>
        </br>
        <p>Kopi Lintong ternyata sudah menjadi kopi favorit masyarakat Eropa sejak
            jaman dahulu kala. Hal ini disebabkan karena kopi Lintong memiliki aroma
            yang harum dan khas serta rasanya yang kuat seperti dark chocolate.
        </p>
        </br>
        <p>Bagaimana? Apakah anda juga baru mengenal kopi dari tanah batak yang ternyata
            begitu mendunia ini. Sebagai masyarakat Indonesia sebaiknya kita memiliki
            pengetahuan mengenai komoditas unggulan negeri sendiri agar warisan Indonesia
            tidak mudah diklaim oleh negara lain. Bagi anda yang ingin mencicipi kopi dari
            tanah batak yang dikenalkan di atas kini sudah tidak sulit lagi.
        </p>
